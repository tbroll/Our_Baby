       <h2> Computer Science Practicum </h2>
	   <hr>

        <p>Winona State University's computer science practicum provides Computer Science majors with experience in a non-academic setting. It is intended to serve as an introduction to an application environment as well as to solidify many of the concepts learned in the classroom. For these reasons, it is to be taken only by students who are nearing the end of their program of study and who have little or no previous work experience. For those students who are currently employed, the practicum must provide them with work experience which is substantially different from any previous or current work experience. 
         </p>
