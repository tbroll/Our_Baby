		<h2>Practicum Requirements</h2>
		<hr>
		
		<h4><span class="glyphicon glyphicon-triangle-bottom
" aria-hidden="true"></span> Eligibility Requirements</h4>
          <div class="answer">
			<p>Students who are considering a practicum should apply by
			completing the "CS 491 Student Information" form. They should
			return it, along with a copy of their student transcript, 
			to the coordinator at least one term before they intend to 
			register for the practicum. To be eligible, students must:</p>
			
			<ol>
				<li>Be a declared CS or CIS major and be admitted in the respective program.</li>
				<li>Have junior or senior status</li>
				<li>Have completed the following courses: CS 341, CS 351, CS 410, [Stat 210 or Math 165], 
				CS 385 or CS 405(old CS 376)], [CS 470 or CS 371(old CS 417)], and [CS 412 or CS 485]. 
				All prerequisites to these courses must be completed with a C grade or better.  
				At least 5 computer science courses must be completed through WSU. A student who
				has failed one or more courses as a result of cheating, according to department 
				records, is not eligible for CS 491.</li>
				<li>Provide the names of two WSU computer science professors, 
				from whom they have taken core or option courses, who have agreed to serve as references.</li>
				<li>Have been granted no more than 3 semester hours of equivalency 
				credit for previous work experience in computer science.</li>
			</ol>
			
			<p>A CS 491 Student Information form can be obtained by contacting
			the coordinator or downloading and printing one.</p>
            
          </div>
		  
		  <h4><span class="glyphicon glyphicon-triangle-bottom
" aria-hidden="true"></span>  Locating a practicum site</h4>
          <div class="answer">
			<p>After receiving formal approval of their application,
			the student will be given a "Practicum Proposal" form and
			may begin the process of locating a practicum site. 
			Locating a site is the responsibility of the student; 
			however, the coordinator may suggest several possible 
			companies so it is advisable to consult with the coordinator
			at this point. To secure a site, you are expected to apply to
			companies, providing them with a cover letter, resume and 
			copy of your transcript, as with a normal job search. If an
			offer is made, you must obtain a description of the tasks to 
			be done, an estimate of the time involved, and the name, address
			and phone number of the person expected to be the supervisor. 
			You must also make the supervisor aware that you expect it to 
			be for academic credit, that they must allow an onsite visit by
			the coordinator, and they must review your written practicum report 
			at the end. If, for any reason, the company does not allow an onsite
			visit or does not allow you to release a report, the proposal will not be approved.</p>
			
			<p>It is likely that the starting date and ending date of the work
			period will not coincide with a school term. Therefore, you should
			obtain these dates from the company to determine the most appropriate
			term in which to register for practicum credit. A common practice 
			is to register for the term in which the work begins and take an
			incomplete grade to extend your student status until the
			practicum work is complete.</p>
            
          </div>
		  
		  <h4><span class="glyphicon glyphicon-triangle-bottom
" aria-hidden="true"></span>  Submitting a proposal for a practicum</h4>
          <div class="answer">
			<p>After an approved student has located a practicum site,
			he/she should complete the Practicum Proposal form and 
			return it to the coordinator at least 6 weeks before 
			they intend to register for the practicum. The purpose 
			of this form is to determine if the site and tasks 
			performed will be suitable for practicum credit. The
			information needed for this will include:</p>
			
			<ol>
				<li>The term in which the student plans to register for the practicum.</li>
				<li>An estimate of the time required to complete the proposed tasks, in hours.</li>
				<li>Beginning and ending dates of the practicum.</li>
				<li>The name, address, and phone number of the supervisor.</li>
				<li>An outline of the tasks to be performed.</li>
			</ol></br>
			
			<p><b>The final approval process</b></p></br>
			
			<p>To be approved, a minimum of 200 hours is required on site, 
			the tasks performed must be related to the concepts introduced
			in the student's computer science course work, and the proposed
			practicum must be substantially different from any previous or 
			current work experience. Final approval rests with the practicum 
			coordinator. The student must have formal approval from the 
			coordinator before he/she can register for the class and before 
			any work is performed. The class, CS 491, is offered for 6 semester
			credits, only 3 of these credits can be applied to a computer 
			science major , and the student receives a P or N grade. Also, 
			there is an additional $20 laboratory fee and, depending on 
			location, students can expect to have at least one on-site 
			visit by the coordinator while they are doing their practicum. 
			During this visit, the coordinator will meet with the student 
			and the supervisor.</p>
			
          </div>
		  
		  <h4><span class="glyphicon glyphicon-triangle-bottom
" aria-hidden="true"></span>  Completing the practicum</h4>
          <div class="answer">
			<p>After an approved student has located a practicum site,
			he/she should complete the Practicum Proposal form and 
			return it to the coordinator at least 6 weeks before 
			they intend to register for the practicum. The purpose
			of this form is to determine if the site and tasks 
			performed will be suitable for practicum credit. 
			The information needed for this will include:</p>
			
			<ol>
				<li>The term in which the student plans to register for the practicum.</li>
				<li>An estimate of the time required to complete the proposed tasks, in hours.</li>
				<li>Beginning and ending dates of the practicum.</li>
				<li>The name, address, and phone number of the supervisor.</li>
				<li>An outline of the tasks to be performed.</li>
			</ol>
          </div>
		  
		  <h4><span class="glyphicon glyphicon-triangle-bottom
" aria-hidden="true"></span>  The final approval process</h4>
          <div class="answer">
			<p>In addition to the tasks designated by the company, 
			to complete the practicum, the student is required to provide the following:</p>
			
			<ol>
				<li>A 3-4 page, typed, report by the student which summarizes the student's
				experience. This report must be submitted to the student's supervisor for approval.</li>
				<li>A report from the student's supervisor, on company stationery,
				confirming the accuracy of the student's report and verifying that 
				the student has not disclosed any confidential information. Both 
				reports must be sent directly from the supervisor to the coordinator
				and are due on the last class day of the term in which the student 
				has registered for the practicum. The student is responsible for 
				supplying the necessary mailing materials.</li>
				<li>An oral report describing the student's practicum experience.
				This report, up to 50 minutes in length, will be given on campus 
				and be open to interested faculty and students.</li>
				<li>A final exit interview with the practicum coordinator.</li>
			</ol>
          </div>
		
		
